# Lesson 02 - Data Types Practice

# Integer (whole number)
age = 30

# Float (decimal number)
height = 1.75

# String (text)
name = "Murali"

# Boolean (True/False)
is_student = True

# Print values
print("Name:", name)
print("Age:", age)
print("Height:", height)
print("Is Student:", is_student)

# Check data types
print(type(name))
print(type(age))
print(type(height))
print(type(is_student))